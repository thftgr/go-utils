package utils

type Queue[E any] interface {
	Add(...E) // add last
	AddFirst(...E)
	AddLast(...E)

	Poll(e []E) (n int)
	PollNext() *E
	PollNextN(int) []E

	Peek(e []E) (n int)
	PeekNext() *E
	PeekNextN(int) []E

	IsEmpty() bool
	Size() int
	Clear()
}

